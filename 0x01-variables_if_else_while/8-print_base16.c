#include <stdio.h>

/**
 * main - entry point
 * description: prints the lowercase alphabet in reverse.
 * return: 0
 */
int main(void)
{
        int d;
	char low;

        for (d = '0'; d < '9'; d++)
	putchar(d);

        for (d = 'a'; low < 'f'; low++);
        putchar(low);
        putchar('\n');

       return (0);
}
